; -------------------------------------------------------------------------
;  STEPPER MOTOR MOVING IN HALF STEPS.
; -------------------------------------------------------------------------
;  It would be better to use a data table. It is not a
;  good idea to mix code and data.
; -------------------------------------------------------------------------

Start:
	MOV	AL,01
	OUT	05
	MOV	AL,03
	OUT	05
	MOV	AL,02
	OUT	05
	MOV	AL,06
	OUT	05
	MOV	AL,04
	OUT	05
	MOV	AL,0c
	OUT	05
	MOV	AL,08
	OUT	05
	MOV	AL,09
	OUT	05

	JMP	Start

; -------------------------------------------------------------------------
	END

